"""
문2)  5행3열의 난수 실수값을 갖는 data 행렬을 대상으로 전체 평균, 
     행축을 기준으로 최댓값, 열축을 기준으로 표준편차 구하여 
     <출력 예시> 처럼 출력하시오.
     
     <출력 예시>
     전체 평균 = 0.536244
     행축을 기준으로 최댓값 = [0.73097447 0.97521494 0.81203633]
     열축을 기준으로 표준편차 = [0.14239376 0.13861899 0.28348128 0.15422305 0.25575217]
"""
import numpy as np 

data = np.random.rand(5, 3)  # 패키지.모듈.함수()

print(data)  # 5행 3열

print('전체 평균 =', data.sum())
print('행축을 기준으로 최댓값 =', data.max(axis=0))
print('열축을 기준으로 표준편차 =', data.std(axis=1))



 